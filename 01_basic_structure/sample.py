# Python의 주요 특징 (Major characteristics of Python)
# 1. 인터프리터 언어 (Interpreter Language)
#    - 컴파일 없이 한 줄씩 바로 실행 가능
# 2. 동적 타이핑 (Dynamic Typing)
#    - 변수 선언 시 자료형을 명시할 필요 없음
# 3. 객체 지향 프로그래밍 (Object-Oriented Programming)
#    - 클래스와 객체를 통한 프로그래밍 지원
# 4. 크로스 플랫폼 (Cross Platform)
#    - Windows, Linux, Mac 등 다양한 운영체제에서 실행 가능
# 5. 풍부한 표준 라이브러리 (Rich Standard Library)
#    - 다양한 기능을 제공하는 내장 모듈 보유

